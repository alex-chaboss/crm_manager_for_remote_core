def normalize_locale(raw: Any) -> Locale:
    s = (str(raw) if raw is not None else "").strip().lower()
    if s == "en":
        return "en"
    return "ru"


def tr(locale: Any, key: str, **kwargs: Any) -> str:
    loc = normalize_locale(locale)
    bucket = _STRINGS.get(loc) or _STRINGS[_FALLBACK]
    text = bucket.get(key) or _STRINGS[_FALLBACK].get(key) or key
    if kwargs:
        return text.format(**kwargs)
    return text
